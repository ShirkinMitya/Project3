package Objects;

public class Kium {

    private Integer kium_id;
    private Float kium_value;
    private Integer kium_year;
    private Integer reactor_id;

    public Integer getKium_id() {
        return kium_id;
    }

    public void setKium_id(Integer kium_id) {
        this.kium_id = kium_id;
    }

    public Float getKium_value() {
        return kium_value;
    }

    public void setKium_value(Float kium_value) {
        this.kium_value = kium_value;
    }

    public Integer getKium_year() {
        return kium_year;
    }

    public void setKium_year(Integer kium_year) {
        this.kium_year = kium_year;
    }

    public Integer getReactor_id() {
        return reactor_id;
    }

    public void setReactor_id(Integer reactor_id) {
        this.reactor_id = reactor_id;
    }
}
